function isInsideBoundingBox(lat, lng) {
  const [[minLat, minLng], [maxLat, maxLng]] = AZERBAIJAN_BOUNDS;
  return lat >= minLat && lat <= maxLat && lng >= minLng && lng <= maxLng;
}

function isPointInPolygon(lat, lng, polygon) {
  let inside = false;

  for (let index = 0, previous = polygon.length - 1; index < polygon.length; previous = index, index += 1) {
    const [currentLng, currentLat] = polygon[index];
    const [previousLng, previousLat] = polygon[previous];

    const intersects = ((currentLat > lat) !== (previousLat > lat))
      && (lng < ((previousLng - currentLng) * (lat - currentLat)) / ((previousLat - currentLat) || Number.EPSILON) + currentLng);

    if (intersects) {
      inside = !inside;
    }
  }

  return inside;
}

export function isWithinAzerbaijan(lat, lng) {
  const normalizedLat = Number(lat);
  const normalizedLng = Number(lng);

  if (!Number.isFinite(normalizedLat) || !Number.isFinite(normalizedLng)) {
    return false;
  }

  if (!isInsideBoundingBox(normalizedLat, normalizedLng)) {
    return false;
  }

  return AZERBAIJAN_POLYGONS.some((polygon) => isPointInPolygon(normalizedLat, normalizedLng, polygon));
}

